import 'dart:math';

areaRect(int length, int width) {
  int area = length * width;
  return area;
}
areaCirc(int radius) {
  double area = pi * (radius * radius);
  return area;
}
areaTri(int base, int height) {
  double area = 0.5 * base * height;
  return area;
}
